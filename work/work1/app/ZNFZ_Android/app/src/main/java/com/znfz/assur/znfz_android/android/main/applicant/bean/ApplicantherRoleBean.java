public class ApplicantherRoleBean {
    private String jobAddress;      // 工作的地址
    private String role;            // 角色名称
    private String rolePercentage;  // 角色每人的百分比
    private String roleCurNum;      // 角色当前剩余需要人数
    private String roleMoney;       // 角色每个人分配的金钱
    private Long roleWorkTimeSp;    // 上班时间戳
    private String companyName;     // 公司名
    private boolean beenAppliciant; // 已被申请


    public String getJobAddress() {
        return jobAddress;
    }

    public void setJobAddress(String jobAddress) {
        this.jobAddress = jobAddress;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getRolePercentage() {
        return rolePercentage;
    }

    public void setRolePercentage(String rolePercentage) {
        this.rolePercentage = rolePercentage;
    }

    public String getRoleCurNum() {
        return roleCurNum;
    }

    public void setRoleCurNum(String roleCurNum) {
        this.roleCurNum = roleCurNum;
    }

    public String getRoleMoney() {
        return roleMoney;
    }

    public void setRoleMoney(String roleMoney) {
        this.roleMoney = roleMoney;
    }

    public Long getRoleWorkTimeSp() {
        return roleWorkTimeSp;
    }

    public void setRoleWorkTimeSp(Long roleWorkTimeSp) {
        this.roleWorkTimeSp = roleWorkTimeSp;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public boolean isBeenAppliciant() {
        return beenAppliciant;
    }

    public void setBeenAppliciant(boolean beenAppliciant) {
        this.beenAppliciant = beenAppliciant;
    }
}
