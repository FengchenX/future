package com.znfz.assur.znfz_android.android.main.publisher.bean;

/**
 * 收入
 * Created by assur on 2018/4/26
 */
public class PublisherIncomeBean {

    private String incomeTitle;            // 收入标题
    private Long incomeTimeSp;             // 收入时间
    private String incomeCount;            // 收入数量
    private int incomeType;                // 收入类型（加：1，减：2）


    public PublisherIncomeBean() {
        this.incomeTitle = "";
        this.incomeTimeSp = new Long(0);
        this.incomeCount = "";
        this.incomeType = 0;
    }

    public String getIncomeTitle() {
        return incomeTitle;
    }

    public void setIncomeTitle(String incomeTitle) {
        this.incomeTitle = incomeTitle;
    }

    public Long getIncomeTimeSp() {
        return incomeTimeSp;
    }

    public void setIncomeTimeSp(Long incomeTimeSp) {
        this.incomeTimeSp = incomeTimeSp;
    }

    public String getIncomeCount() {
        return incomeCount;
    }

    public void setIncomeCount(String incomeCount) {
        this.incomeCount = incomeCount;
    }

    public int getIncomeType() {
        return incomeType;
    }

    public void setIncomeType(int incomeType) {
        this.incomeType = incomeType;
    }

    @Override
    public String toString() {
        return "IncomeBean{" +
                "incomeTitle='" + incomeTitle + '\'' +
                ", incomeTimeSp='" + incomeTimeSp + '\'' +
                ", incomeCount='" + incomeCount + '\'' +
                ", incomeType=" + incomeType +
                '}';
    }
}
