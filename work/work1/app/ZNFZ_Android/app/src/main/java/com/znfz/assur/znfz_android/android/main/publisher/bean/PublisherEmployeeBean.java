package com.znfz.assur.znfz_android.android.main.publisher.bean;

/**
 * 员工
 * Created by assur on 2018/4/26
 */
public class PublisherEmployeeBean {

    private String publisherEmployeeName;  // 员工姓名
    private String publisherEmployeePhone; // 员工手机号
    private String publisherEmployeeRole;  // 员工角色
    private String publisherEmployeeAddress;  // 员工账户地址
    private long publisherEmployeePer;         // 员工分账百分比

    public PublisherEmployeeBean() {
        this.publisherEmployeeName = "";
        this.publisherEmployeePhone = "";
        this.publisherEmployeeRole = "";
        this.publisherEmployeeAddress = "";
        this.publisherEmployeePer = 0;
    }

    public String getPublisherEmployeeName() {
        return publisherEmployeeName;
    }

    public void setPublisherEmployeeName(String publisherEmployeeName) {
        this.publisherEmployeeName = publisherEmployeeName;
    }

    public String getPublisherEmployeePhone() {
        return publisherEmployeePhone;
    }

    public void setPublisherEmployeePhone(String publisherEmployeePhone) {
        this.publisherEmployeePhone = publisherEmployeePhone;
    }

    public String getPublisherEmployeeRole() {
        return publisherEmployeeRole;
    }

    public void setPublisherEmployeeRole(String publisherEmployeeRole) {
        this.publisherEmployeeRole = publisherEmployeeRole;
    }

    public String getPublisherEmployeeAddress() {
        return publisherEmployeeAddress;
    }

    public void setPublisherEmployeeAddress(String publisherEmployeeAddress) {
        this.publisherEmployeeAddress = publisherEmployeeAddress;
    }

    public long getPublisherEmployeePer() {
        return publisherEmployeePer;
    }

    public void setPublisherEmployeePer(long publisherEmployeePer) {
        this.publisherEmployeePer = publisherEmployeePer;
    }

    @Override
    public String toString() {
        return "PublisherEmployeeBean{" +
                "publisherEmployeeName='" + publisherEmployeeName + '\'' +
                ", publisherEmployeePhone='" + publisherEmployeePhone + '\'' +
                ", publisherEmployeeRole='" + publisherEmployeeRole + '\'' +
                ", publisherEmployeeAddress='" + publisherEmployeeAddress + '\'' +
                ", publisherEmployeePer='" + publisherEmployeePer + '\'' +
                '}';
    }
}
